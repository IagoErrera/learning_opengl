Cube::Cube(Shader* shader, Texture2D* texture) : 
	shader{shader},
   	texture{texture}, 
	transform(Transform(shader, "model"))
{
	if (!Cube::initialized) initDrawer();	
}

void Cube::draw(Shader* s) const {
	glBindVertexArray(Cube::VAO);
	s->use();
	texture->activate();
	transform.update();
	glDrawArrays(GL_TRIANGLES, 0, 36);
}
	
void Cube::draw() const {
	glBindVertexArray(Cube::VAO);
	shader->use();
	texture->activate();
	transform.update();
	glDrawArrays(GL_TRIANGLES, 0, 36);
}
